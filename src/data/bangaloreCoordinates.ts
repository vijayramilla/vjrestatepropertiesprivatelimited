/** Resolve lat/lng from a locality string (exact or partial match). */
export function resolveBangaloreCoordinates(
  locality: string | null | undefined,
): { lat: number; lng: number } | null {
  if (!locality || typeof locality !== 'string') return null;

  const trimmed = locality.trim();
  if (BANGALORE_COORDINATES[trimmed]) return BANGALORE_COORDINATES[trimmed];

  const lower = trimmed.toLowerCase();
  const exactKey = Object.keys(BANGALORE_COORDINATES).find(
    (k) => k.toLowerCase() === lower,
  );
  if (exactKey) return BANGALORE_COORDINATES[exactKey];

  const partialKey = Object.keys(BANGALORE_COORDINATES).find(
    (k) => lower.includes(k.toLowerCase()) || k.toLowerCase().includes(lower),
  );
  return partialKey ? BANGALORE_COORDINATES[partialKey] : null;
}

/** Canonical locality keys — single source for map + admin dropdowns */
export const MAP_LOCALITY_NAMES = Object.keys(BANGALORE_COORDINATES).sort();

/** Resolve user/admin input to a canonical MAP locality name */
export function resolveMapLocalityName(locality: string): string | null {
  if (!locality || typeof locality !== 'string') return null;

  const trimmed = locality.trim();
  if (BANGALORE_COORDINATES[trimmed]) return trimmed;

  const lower = trimmed.toLowerCase();
  const exactKey = Object.keys(BANGALORE_COORDINATES).find((k) => k.toLowerCase() === lower);
  if (exactKey) return exactKey;

  const partialKey = Object.keys(BANGALORE_COORDINATES).find(
    (k) => lower.includes(k.toLowerCase()) || k.toLowerCase().includes(lower),
  );
  return partialKey ?? null;
}

export function localityFromGooglePlace(place: google.maps.places.PlaceResult): string | null {
  const candidates = [
    place.name ?? '',
    place.formatted_address ?? '',
    ...(place.address_components ?? []).flatMap((c) => [c.long_name, c.short_name]),
  ];

  for (const candidate of candidates) {
    const matched = resolveMapLocalityName(candidate);
    if (matched) return matched;
  }

  return null;
}
